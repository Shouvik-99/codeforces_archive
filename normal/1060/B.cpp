void solve(){
	ll n;
	cin >> n;
	ll N = n;
	ll y = 0;
	while(n){
		y++;
		n/=10;
	}
	y--;
	ll ans = y*9;
	ll j = 0;
	while(y){
		j = j*10+9;
		y--;
	}
	j = N-j;
	while(j){
		ans += j%10;
		j/=10;
	}
	cout << ans;
}

